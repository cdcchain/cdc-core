        void UbGntpNotifier::client_is_shutting_down()
        {
            my->_shutting_down = true;
        }

        void UbGntpNotifier::notify_connection_count_changed(uint32_t new_connection_count)
        {
            // notify any time we drop below the threshold, unless we've already done so recently
            // (to cut down on noise if we're oscillating around the threshold)
            if (new_connection_count >= my->_connection_count_notification_threshold)
                my->_last_reported_connection_count = new_connection_count;
            else if (new_connection_count < my->_connection_count_notification_threshold &&
                my->_last_reported_connection_count >= my->_connection_count_notification_threshold)
            {
                fc::time_point notification_time_cutoff = fc::time_point::now() - my->_connection_count_notification_interval;
                if (my->_last_connection_count_notification_time < notification_time_cutoff)
                {
                    std::ostringstream message;
                    message << my->_cdc_instance_identifier << ": peer connection count dropped to " << new_connection_count <<
                        ", which is below the warning threshold of " << my->_connection_count_notification_threshold;
                    my->_notifier.send_notification("connection_count_below_threshold", "Connection Count Below Threshold", message.str(), my->_cdc_icon);
                    my->_last_reported_connection_count = new_connection_count;
                    my->_last_connection_count_notification_time = fc::time_point::now();
                }
            }
        }

        void UbGntpNotifier::notify_client_exiting_unexpectedly()
        {
            std::ostringstream message;
            message << my->_cdc_instance_identifier << ": client is exiting due to an unhandled exception";
            my->_notifier.send_notification("client_exiting_unexpectedly", "Client Exiting Unexpectedly", message.str(), my->_cdc_icon);
        }

        void UbGntpNotifier::notify_head_block_too_old(const fc::time_point_sec head_block_age)
        {
            fc::time_point block_age_cutoff = fc::time_point::now() - fc::seconds(CDC_BLOCKCHAIN_BLOCK_INTERVAL_SEC * my->_missed_block_count_threshold);
            if (head_block_age < block_age_cutoff)
            {
                fc::time_point notification_time_cutoff = fc::time_point::now() - my->_head_block_too_old_notification_interval;
                if (my->_last_head_block_too_old_notification_time < notification_time_cutoff)
                {
                    std::ostringstream message;
                    uint32_t age_in_sec = cdcchain::consensus::now().sec_since_epoch() - head_block_age.sec_since_epoch();
                    uint32_t missed_block_count = age_in_sec / CDC_BLOCKCHAIN_BLOCK_INTERVAL_SEC;
                    message << my->_cdc_instance_identifier << ": the last block on our blockchain is " << fc::get_approximate_relative_time_string(head_block_age, cdcchain::consensus::now(), " old") <<
                        ", meaning we've missed " << missed_block_count << " blocks";
                    my->_notifier.send_notification("head_block_too_old", "Head Block is Too Old", message.str(), my->_cdc_icon);
                    my->_last_head_block_too_old_notification_time = fc::time_point::now();
                }
            }
        }


